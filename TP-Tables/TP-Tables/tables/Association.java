package tables;

/**
 * Association cle - valeur 
 */

public class Association<K, V> {

    protected K key ;  // cle
    protected V value ; // valeur 

    /** 
	@param k la cle
	@param v la valeur
     */
    public Association(K k, V v) {
	key = k ;
	value = v ;
    } 
   
    /** Acces a la cle
     */
    public K getKey () {
	return key ;
    } 

    /** Acces a la valeur
     */
    public V getValue () {
	return value ;
    } 

    /** Version String < clŽ, valeur >
     */
    public String toString () {
	return "<"+key+","+value+">" ;
    } 
 
} 

